<h3>
    <b><center> Bem-vindo a UTOME - Clinica Médica </center></b>
</h3>
<p>
A UTOME é uma clínica privada centrada em cuidados diários mínimamente invasivos (Day Care Clinic) multidisciplinares especializados. A UTOME é uma clínica que faz atendimentos de segunda à sexta, das 8h às 20h, desde consultas, exames de diagnósticos. Os actos médicos cirúrgicos mínimamente invasivos são feitos de segunda à sexta, mediante marcação prévia. Actualmente, a clínica modernizou-se, construindo de raíz uma infraestrutura de três pisos com novos serviços, incluindo de saúde materno infantil.
<br>
A clínica tem duas (2) salas de cirurgia com capacidade de quinze (15) camas para internamento. No terceiro piso está em preparação a 3ª sala de cirurgia a ser equipada com OR1. O nosso objectivo de sempre é servir melhor aos pacientes.
Todos os colaboradores bem treinados colocam forte ênfase na qualidade e eficiência nas suas tarefas diárias, determinados a manter a excelência para alcançar a satisfação do paciente.
</p>

